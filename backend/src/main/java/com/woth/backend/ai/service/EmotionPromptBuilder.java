package com.woth.backend.ai.service;

import org.springframework.stereotype.Component;

// 감정 분석을 위한 프롬프트를 생성하는 컴포넌트 클래스
@Component
public class EmotionPromptBuilder {

  public String build(String content) {

    // 획일적인 답변 생성을 막고, 감정이 없는 건조한 사실 기술 문맥에도 유연하게 대응하도록 규칙 고도화
    return """
         사용자가 private room에 남긴 일기나 글의 정서를 정밀하게 분석해서 가장 어울리는 창밖 날씨를 하나 선택해라.

         아래 JSON 형식으로만 반환한다.

         {
           "weatherKey": "rainy",
           "weatherLabel": "비",
           "confidence": 0.82,
           "reason": "분석된 글의 톤앤매너와 날씨 선정 근거를 자연스럽게 설명하는 문장"
         }

         핵심 규칙:
         - weatherKey는 반드시 sunny, cloudy, rainy, sunset, night, dawn, cherry 중 하나만 사용한다.
         - weatherLabel은 weatherKey와 반드시 아래처럼 매칭한다.
           - sunny: 맑음
           - cloudy: 흐림
           - rainy: 비
           - sunset: 노을
           - night: 밤
           - dawn: 오로라
           - cherry: 벚꽃
         - confidence는 선택 확신도를 0.0 이상 1.0 이하의 숫자로 반환한다.
         - reason 작성 규칙:
           1. "사용자의 글에서 ~ 감정 단서가 나타나" 혹은 "~ 선택했습니다" 같은 기계적이고 틀에 박힌 반복 표현은 절대 금지한다. 매번 생동감 있고 다채로운 문장 구조를 사용해라.
           2. [중요] 사용자가 감정을 배제하고 사물, 일상적 행동, 건조한 사실 위주로만 담담하게 일기를 썼을 경우:
              - 존재하지 않는 감정이나 억지 위로, 인위적인 공감을 지어내어 기술하지 말 것.
              - 글의 담백한 톤을 그대로 유지하며, 감정 기복이 없는 평온한 일상 사실 기록이기에 왜 해당 날씨(예: 맑음 또는 흐림)를 부여했는지 객관적이고 담백한 논조의 한 문장으로 설명한다.
           3. 글에 기쁨, 슬픔, 무기력 등 감정선이 풍부하게 녹아있을 경우:
              - 그 감정에 깊이 공감하며 글쓴이의 감정적 여운이나 무드를 반영한 문장으로 부드럽게 근거를 작성한다.

         날씨 선택 기준:
         - 맑음(sunny): 가벼움, 회복감, 기쁨, 안정감 혹은 평온하고 담담한 일상이 뚜렷할 때 선택한다.
         - 흐림(cloudy): 막막함, 무기력, 애매한 불안, 혐오, 혼란, 에너지가 정체되어 있을 때 선택한다.
         - 비(rainy): 슬픔, 외로움, 무거움, 울적함, 우울함, 차분하게 가라앉은 분위기가 강할 때 선택한다.
         - 노을(sunset): 분노, 화가나는 감정, 격렬한 감정의 기복, 강한 갈등이 느껴질 때 선택한다.
         - 밤(night): 고독, 상념, 어두운 감정, 두려움, 피곤함, 내면의 갈등이 강할 때 선택한다.
         - 오로라(dawn): 깊은 생각, 놀람, 그리움, 새로운 시작의 희망이 함께할 때 선택한다.
         - 벚꽃(cherry): 설렘, 일시적인 아름다움, 덧없음, 애틋함, 봄의 감성이 느껴질 때 선택한다.
         반드시 다른 텍스트 없이 완벽한 JSON 오브젝트 하나만 반환해라.

         사용자 입력 글:
         %s
        \s""".formatted(content);
  }
}